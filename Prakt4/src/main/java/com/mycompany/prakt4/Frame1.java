    private void ListTugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListTugasMouseClicked
         if (evt.getClickCount() == 1) {
            int pilihIndex = ListTugas.locationToIndex(evt.getPoint());
            if (pilihIndex >= 0) {
                // Mendapatkan nilai dari elemen 
                String pilihdetail = list2.getElementAt(pilihIndex);
                txtdetail2.setText(pilihdetail);
            }
        }
    }//GEN-LAST:event_ListTugasMouseClicked

    private void bbersihkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bbersihkanActionPerformed

        if (list1.isEmpty() && list2.isEmpty()){
        JOptionPane.showMessageDialog(this,"Data Telah diSimpan");
        }else{
        list1.clear();
        list2.clear();
        detail ="";
         txtdetail2.setText(detail);
         angka=1;
        }
         
       
    }//GEN-LAST:event_bbersihkanActionPerformed


