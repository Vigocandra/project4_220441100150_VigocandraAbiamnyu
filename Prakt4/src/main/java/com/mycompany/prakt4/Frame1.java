    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
       
         matkul = txtmatkul.getText();
         detail = txtdetail.getText();
       
       deadline = txtdeadline.getText();
        
             if (matkul.isEmpty()|| detail.isEmpty()||deadline.isEmpty() ){
            JOptionPane.showMessageDialog(this,"Masukan Data Dengan Benar!! dan isi Semuanya");
//                 System.out.println("berhasil");
         }else {
             
         list1.addElement(angka + "." + matkul + " (DL : " + deadline + ")");
         list2.addElement(detail);

          JOptionPane.showMessageDialog(this,"Data Telah diSimpan");
          angka++;
//           System.out.println("tidak");
         }
         

     
    }//GEN-LAST:event_bsimpanActionPerformed

